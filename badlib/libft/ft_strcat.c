char	*ft_strcat(char *dst, const char *src)
{
	int n;
	int a;

	n = 0;
	a = 0;
	while (dst[n])
		n++;
	while (src[a])
	{
		dst[n + a] = src[a];
		a++;
	}
	dst[n + a] = '\0';
	return (dst);
}
